#!/usr/bin/env node
/**
 * Generate src/generated/manifest.ts from Laravel's own route table.
 *
 * Source of truth: `php artisan route:list --json --path=api/v1` run in the
 * Tickiti app dir. We parse middleware to recover each endpoint's abilities,
 * required role, sysadmin gate and plan gates — none of which we want to
 * hand-transcribe. Re-run whenever routes/api.php changes.
 *
 *   TICKITI_DIR=e:/devl/tickiti node scripts/build-manifest.mjs
 */
import { execFileSync } from "node:child_process";
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const TICKITI_DIR = process.env.TICKITI_DIR ?? "e:/devl/tickiti";
const HERE = dirname(fileURLToPath(import.meta.url));
const OUT = join(HERE, "..", "src", "generated", "manifest.ts");

// Idempotent writes: the two endpoints whose controllers require an
// Idempotency-Key header (ApiController::create_ticket / ::ticket_respond).
const IDEMPOTENT = new Set(["api.v1.tickets.create", "api.v1.tickets.respond"]);

function fetchRoutes() {
  const raw = execFileSync("php", ["artisan", "route:list", "--json", "--path=api/v1"], {
    cwd: TICKITI_DIR,
    encoding: "utf8",
    maxBuffer: 32 * 1024 * 1024,
  });
  return JSON.parse(raw);
}

function parseMiddleware(mw) {
  const abilities = [];
  const plans = [];
  const roles = [];
  let sysadmin = false;

  for (const m of mw) {
    if (m.startsWith("Laravel\\Sanctum\\Http\\Middleware\\CheckForAnyAbility:")) {
      abilities.push(...m.split(":").slice(1).join(":").split(","));
    } else if (m.startsWith("Laravel\\Sanctum\\Http\\Middleware\\CheckAbilities:")) {
      abilities.push(...m.split(":").slice(1).join(":").split(","));
    } else if (m.startsWith("App\\Http\\Middleware\\CheckPlanAccess:")) {
      plans.push(...m.split(":").slice(1).join(":").split(","));
    } else if (m.endsWith("Middleware\\Admin")) {
      roles.push("admin");
    } else if (m.endsWith("Middleware\\Staff")) {
      roles.push("staff");
    } else if (m.endsWith("Middleware\\Superuser")) {
      roles.push("superuser");
    } else if (m.endsWith("Middleware\\SysAdmin")) {
      sysadmin = true;
    }
  }

  // Highest role wins for display purposes.
  const role =
    roles.find((r) => r === "superuser") ??
    roles.find((r) => r === "admin") ??
    roles.find((r) => r === "staff") ??
    null;

  return { abilities, plans, role, sysadmin };
}

function toEntry(route) {
  const name = route.name ?? "";
  const parts = name.split("."); // api.v1.<family>.<action...>
  const family = parts[2] ?? "";
  const action = parts.slice(3).join(".") || "(root)";
  const uri = route.uri.replace(/^api\/v1\//, ""); // path passed to callV1
  const params = [...route.uri.matchAll(/\{(\w+)\}/g)].map((m) => m[1]);
  const { abilities, plans, role, sysadmin } = parseMiddleware(route.middleware ?? []);

  return {
    name,
    family,
    action,
    method: route.method.split("|")[0],
    uri,
    abilities,
    role,
    sysadmin,
    plans,
    params,
    idempotent: IDEMPOTENT.has(name),
    write: abilities.length > 0 && !abilities.some((a) => a.endsWith(":read")),
  };
}

function main() {
  const routes = fetchRoutes()
    .filter((r) => typeof r.name === "string" && r.name.startsWith("api.v1."))
    .map(toEntry)
    .sort((a, b) => a.name.localeCompare(b.name));

  const banner =
    `// AUTO-GENERATED by scripts/build-manifest.mjs — DO NOT EDIT.\n` +
    `// Regenerate: TICKITI_DIR=${TICKITI_DIR} node scripts/build-manifest.mjs\n` +
    `// ${routes.length} endpoints across ${new Set(routes.map((r) => r.family)).size} families.\n\n`;

  const body =
    `export interface RouteEntry {\n` +
    `  name: string;\n` +
    `  family: string;\n` +
    `  action: string;\n` +
    `  method: string;\n` +
    `  uri: string;\n` +
    `  abilities: string[];\n` +
    `  role: string | null;\n` +
    `  sysadmin: boolean;\n` +
    `  plans: string[];\n` +
    `  params: string[];\n` +
    `  idempotent: boolean;\n` +
    `  write: boolean;\n` +
    `}\n\n` +
    `export const MANIFEST: RouteEntry[] = ${JSON.stringify(routes, null, 2)};\n`;

  mkdirSync(dirname(OUT), { recursive: true });
  writeFileSync(OUT, banner + body);
  console.log(`Wrote ${OUT} (${routes.length} endpoints).`);
}

main();
